import mongoose from "mongoose";

const ChatRequestSchema = mongoose.Schema(
  {
    senderId: {
      type: String,
      required: true,
    },
    receiverId: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      enum: ["pending", "accepted", "declined"],
      default: "pending",
    },
  },
  { timestamps: true }
);

const ChatRequest = mongoose.model("ChatRequest", ChatRequestSchema);

export default ChatRequest;
